let redCount = 2;

    document.getElementById("red-subtract").onclick = function()
    {
        redCount-=1;
        document.getElementById("red-number").innerHTML = redCount;

        if(redCount < 0)
        {
            redCount=0;
            document.getElementById("red-number").innerHTML = redCount;
        }

    }

    document.getElementById("red-add").onclick = function()
    {
        redCount+=1;
        document.getElementById("red-number").innerHTML = redCount;
        
    }
